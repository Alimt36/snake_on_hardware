
`timescale 1ns / 1ps

//---------------------------------------------------------------------------------------------------------------------------
// the display is a dotmatrix : 8 row * 8 col 
//      --> R is HIGHs
//      --> C is LOWs
//
//  (count_to) parameter is the amount of time that every row has for being on!
//---------------------------------------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------------------------------------
module dotmatrix_interface #(parameter count_to = 2000)(
    output reg [7:0] R,
    output reg [7:0] C,

    input [63:0] shape_flat,

    input wire clk,
    input wire rst
);

//---------------------------------------------------------------------------------------------------------------------------
    localparam COUNTER_WIDTH = $clog2(count_to + 1);
    reg [2:0]                  row_index;
    reg [COUNTER_WIDTH-1 : 0]  counter;
//---------------------------------------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------------------------------------
// Counter: counts up to count_to then resets
//---------------------------------------------------------------------------------------------------------------------------
    always @ (posedge clk or posedge rst) begin
        if (rst) begin
            counter <= 0;
        end
        else begin
            if (counter == count_to) begin
                counter <= 0;
            end
            else begin
                counter <= counter + 1;
            end
        end
    end
//---------------------------------------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------------------------------------
// Row scanner state machine
//---------------------------------------------------------------------------------------------------------------------------
    always @ (posedge clk or posedge rst) begin
        if (rst) begin
            R         <= 8'b0000_0000;
            C         <= 8'b0000_0000;
            row_index <= 3'd0;
        end
        else if (counter == count_to) begin
            case (row_index)
            3'd0 : begin R <= 8'b0000_0001; C <= ~(shape_flat[ 7: 0]); row_index <= 3'd1; end
            3'd1 : begin R <= 8'b0000_0010; C <= ~(shape_flat[15: 8]); row_index <= 3'd2; end
            3'd2 : begin R <= 8'b0000_0100; C <= ~(shape_flat[23:16]); row_index <= 3'd3; end
            3'd3 : begin R <= 8'b0000_1000; C <= ~(shape_flat[31:24]); row_index <= 3'd4; end
            3'd4 : begin R <= 8'b0001_0000; C <= ~(shape_flat[39:32]); row_index <= 3'd5; end
            3'd5 : begin R <= 8'b0010_0000; C <= ~(shape_flat[47:40]); row_index <= 3'd6; end
            3'd6 : begin R <= 8'b0100_0000; C <= ~(shape_flat[55:48]); row_index <= 3'd7; end
            3'd7 : begin R <= 8'b1000_0000; C <= ~(shape_flat[63:56]); row_index <= 3'd0; end

            default: begin R <= 8'b0000_0000; C <= 8'b0000_0000; row_index <= 3'd0; end
            endcase
        end
    end
//---------------------------------------------------------------------------------------------------------------------------

endmodule
